                        var mnext = (100-$('.bio-slick:visible').find('.slick-next span').height())/2;
                        $('.bio-slick:visible').find('.slick-next span').css('margin-top', mnext)

                        history.pushState(null, '404', $('.bio-item.slick-current:visible').data('url'))
                    });
